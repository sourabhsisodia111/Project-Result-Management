    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String roll = jTextField1.getText();
        String physics = jTextField2.getText();
        String maths = jTextField3.getText();
        String chemistry = jTextField4.getText();
        String dsa = jTextField5.getText();
        String dbms = jTextField6.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/result", "root", "root");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+roll+"'");
            if(rs.next())
            {
                st.executeUpdate("insert into reuslttable(roll, physics, maths, chemistry, dsa, dbms) value('"+roll+"','"+physics+"','"+maths+"','"+chemistry+"', '"+dsa+"', '"+dbms+"')");
                JOptionPane.showMessageDialog(null, "Marks Saved Successfully!");
                setVisible(false);
                new addResult().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Roll Number doesn't exist!");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

