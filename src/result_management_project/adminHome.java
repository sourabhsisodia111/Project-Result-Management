    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String course = (String)jComboBox1.getSelectedItem();
        String branchName = (String)jComboBox2.getSelectedItem();
        String name = jTextField2.getText();
        String gender = (String)jComboBox3.getSelectedItem();
        String fatherName = jTextField3.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/result", "root", "root");
            Statement st = con.createStatement();
            st.executeUpdate("insert into student(rollno, course, branchName, name, gender, fatherName) value('"+rollno+"','"+course+"','"+branchName+"','"+name+"', '"+gender+"', '"+fatherName+"')");
            JOptionPane.showMessageDialog(null, "Data Saved Successfully");
            setVisible(false);
            new adminHome().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_saveActionPerformed

