    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        String rollno = jTextField2.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/result", "root", "root");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student inner join reuslttable where student.rollno='"+rollno+"' and reuslttable.roll='"+rollno+"'");
            if(rs.next())
            {
                jTextField1.setText(rs.getString(4));
                jTextField4.setText(rs.getString(3));
                jTextField5.setText(rs.getString(2));
                jTextField6.setText(rs.getString(5));
                jTextField3.setText(rs.getString(6));
                jTextField18.setText(rs.getString(8));
                jTextField19.setText(rs.getString(9));
                jTextField20.setText(rs.getString(10));
                jTextField21.setText(rs.getString(11));
                jTextField22.setText(rs.getString(12));
                int m1 = Integer.parseInt(jTextField18.getText());
                m1 += Integer.parseInt(jTextField19.getText());
                m1 += Integer.parseInt(jTextField20.getText());
                m1 += Integer.parseInt(jTextField21.getText());
                m1 += Integer.parseInt(jTextField22.getText());
                jTextField8.setText(m1+"");
//                jTextField1.setText(rs.getString(2));
            }
            else
            {
                
                JOptionPane.showMessageDialog(null, "Student Record Doesn't Exit!");
                setVisible(false);
                new StudentHome().setVisible(true);
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

